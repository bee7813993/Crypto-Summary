var _lang = localStorage.getItem("cs_lang") || "ja";

function t(key, vars) {
  var str =
    (TRANSLATIONS[_lang] && TRANSLATIONS[_lang][key] !== undefined)
      ? TRANSLATIONS[_lang][key]
      : (TRANSLATIONS["ja"] && TRANSLATIONS["ja"][key] !== undefined)
        ? TRANSLATIONS["ja"][key]
        : key;

  if (vars) {
    str = str.replace(/\{(\w+)\}/g, function (match, name) {
      return vars[name] !== undefined ? vars[name] : match;
    });
  }

  return str;
}

function setLang(lang) {
  _lang = lang;
  localStorage.setItem("cs_lang", lang);
  applyI18n();
}

function applyI18n() {
  document.documentElement.lang = _lang;

  var i18nEls = document.querySelectorAll("[data-i18n]");
  for (var i = 0; i < i18nEls.length; i++) {
    var el = i18nEls[i];
    el.textContent = t(el.getAttribute("data-i18n"));
  }

  var placeholderEls = document.querySelectorAll("[data-i18n-placeholder]");
  for (var j = 0; j < placeholderEls.length; j++) {
    var pel = placeholderEls[j];
    pel.placeholder = t(pel.getAttribute("data-i18n-placeholder"));
  }

  var titleEls = document.querySelectorAll("[data-i18n-title]");
  for (var k = 0; k < titleEls.length; k++) {
    var tel = titleEls[k];
    tel.title = t(tel.getAttribute("data-i18n-title"));
  }
}

document.addEventListener("DOMContentLoaded", applyI18n);
